using System;
using System.Collections.Generic;
using System.Text;

namespace SkillMine.LoopsProgram.ForLoop
{
    class Display75To61
    {
        static void Main(string[] args)
        {
            for(int i = 75; i >= 61; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
